import turtle

t=turtle.Turtle()
#changing shape size of turtle
t.shapesize(3,3,3)
#changing color
t.fillcolor("red")
t.pencolor("yellow")
#changing color in single command
t.color("blue", "orange")
#shanging shapes
t.shape("square")
t.shape("arrow")
t.shape("turtle")
t.shape("triangle")
t.shape("circle")
t.shape("classic")
#set speed of turtle
t.speed(1) #slow spees
#setting pensize
t.pensize(1) #small
#move
t.fd(100)
t.rt(90)
#fast speed
t.speed(10)
#big pensize
t.pensize(10)
t.fd(100)